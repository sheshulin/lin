package cn.mmvtc.fwechat;

public class ContactsInfo {
    private String name;
    private String user;

    public ContactsInfo(String name, String user) {
        this.name = name;
        this.user = user;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }
}
